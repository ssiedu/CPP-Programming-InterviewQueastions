#include<iostream>
using namespace std;
int main()
{
	cout<<"\n size of integer : "<<sizeof(int)<<" bytes";
	cout<<"\n size of float   : "<<sizeof(float)<< " bytes";
	cout<<"\n size of char    : "<<sizeof(char)<<" bytes";
	cout<<"\n size of long    : "<<sizeof(long)<<" bytes";
	cout<<"\n size of double  : "<<sizeof(double)<<" bytes";
	cout<<"\n size of short   : "<<sizeof(short)<< " bytes";
	return 0;
}
